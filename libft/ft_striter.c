#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int		i;

	if (s != NULL && f != NULL)
	{
		i = ft_strlen(s);
		while (i--)
		{
			(*f)(s);
			s++;
		}
	}
}
